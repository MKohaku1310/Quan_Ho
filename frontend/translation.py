def t(key: str) -> str:
    """Returns the translated string for the given key based on current user language."""
    lang = app.storage.user.get('language', 'vi')
    return TRANSLATIONS.get(lang, TRANSLATIONS['vi']).get(key, key)

def toggle_language():
    """Toggles language between 'vi' and 'en'."""
    current = app.storage.user.get('language', 'vi')
    new_lang = 'en' if current == 'vi' else 'vi'
    app.storage.user['language'] = new_lang

def tc(obj, field: str) -> str:
    """
    Translated Content helper. Returns the English version of a field if the
    current language is English and the field exists, otherwise falls back to Vietnamese.
    """
    if not obj:
        return ""
    
    lang = app.storage.user.get('language', 'vi')
    if lang == 'en':
        field_en = f"{field}_en"
        val_en = obj.get(field_en)
        if val_en:
            return val_en
            
    # Fallback to default (Vietnamese)
    return obj.get(field, "")
