def t(key: str) -> str:
    """Returns the translated string for the given key based on current user language."""
    lang = app.storage.user.get('language', 'vi')
    return TRANSLATIONS.get(lang, TRANSLATIONS['vi']).get(key, key)

def toggle_language():
    """Toggles language between 'vi' and 'en'."""
    current = app.storage.user.get('language', 'vi')
    new_lang = 'en' if current == 'vi' else 'vi'
    app.storage.user['language'] = new_lang
